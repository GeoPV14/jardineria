package com.jardineria.bean;

public class EmpleadosCountBean {
	private Long totalEmpleados;

	public EmpleadosCountBean() {
		super();
	}

	public EmpleadosCountBean(Long totalEmpleados) {
		super();
		this.totalEmpleados = totalEmpleados;
	}

	public Long getTotalEmpleados() {
		return totalEmpleados;
	}

	public void setEmpleados(Long totalEmpleados) {
		this.totalEmpleados = totalEmpleados;
	}
	
	

}
